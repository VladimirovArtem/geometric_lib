def area(a):
    """
    Функция для вычисления площади квадрата.

    Аргументы:
    a -- длина стороны квадрата

    Возвращает:
    Площадь квадрата, вычисленная по формуле: a^2
    """
    return a * a  # Возвращаем площадь квадрата

def perimeter(a):
    """
    Функция для вычисления периметра квадрата.

    Аргументы:
    a -- длина стороны квадрата

    Возвращает:
    Периметр квадрата, вычисленный по формуле: 4 * a
    """
    return 4 * a  # Возвращаем периметр квадрата
